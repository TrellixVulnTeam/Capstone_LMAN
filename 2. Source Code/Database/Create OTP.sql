USE CapstonesNoRelation
GO

DROP TABLE IF EXISTS OTP
CREATE TABLE OTP
(
	ID INT PRIMARY KEY IDENTITY,
	Code INT NOT NULL,
)
GO

DROP PROC IF EXISTS AddOTP
GO
CREATE PROC AddOTP
@code INT
AS
BEGIN
	INSERT dbo.OTP
	(
	    Code
	)
	OUTPUT Inserted.ID
	VALUES
	(@code)
END
GO
DROP PROC IF EXISTS DeleteOTP
GO
CREATE PROC DeleteOTP
@id INT
AS
BEGIN
	DELETE FROM OTP WHERE ID = @id
END
GO